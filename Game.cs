using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace RPG_Final
{
    class Game
    {
        public Encounter[] encounters;
        public Room[] rooms;

        public int enCounter()
        {
            int encounter = 0;
            int cnt = 0;
            //counts the encounter
            for (int x = 0; x < encounter; x++)
            {
                cnt += encounter;
            }
            return cnt;
        }
    }
}
